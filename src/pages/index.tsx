import { SignInButton, SignOutButton, useUser } from "@clerk/nextjs";
import Avatar from "react-avatar";
import { useState } from "react";
import Head from "next/head";
// import Image from "next/image";

import { api } from "~/utils/api";
import { LoadingPage } from "~/components/loading";
import { Sidebar } from "~/components/home/sidebar";
import { Header } from "~/components/home/header";
import { Body } from "~/components/home/body";

export interface SidebarProps {
  isCollapsed: boolean;
  toggleSidebar: () => void;
}

export default function Home() {
  const { isLoaded: userLoaded, isSignedIn } = useUser();
  const [isCollapsed, setIsCollapsed] = useState(false);

  const toggleSidebar = () => setIsCollapsed(!isCollapsed);
  // start fetching to use cached data
  api.base.getAll.useQuery();

  if (!userLoaded) {
    return <div />;
  }

  return (
    <>
      <Head>
        <title>Airtable Clone Home Page</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
        <link
          href="https://fonts.googleapis.com/icon?family=Material+Icons"
          rel="stylesheet"
        />
      </Head>
      <main className="flex">
        <div className="flex flex-row">
          <div className="fixed h-12 w-full border-b border-gray-300 bg-white p-2">
            {!isSignedIn && (
              <div className="flex justify-center">
                <SignInButton />{" "}
              </div>
            )}
            {isSignedIn && (
              <div>
                <Header
                  isCollapsed={isCollapsed}
                  toggleSidebar={toggleSidebar}
                />
              </div>
            )}
          </div>
        </div>

        <div className="flex flex-row">
          <div className="fixed mt-12 bg-white">
            <Sidebar isCollapsed={isCollapsed} toggleSidebar={toggleSidebar} />
          </div>
          <div
            className={`mt-12 transition-all duration-300 ${isCollapsed ? "ml-20" : "ml-80"}`}
          >
            <Body />
          </div>
        </div>
      </main>
    </>
  );
}
